#include "includes/libft.h"

void
	*ft_memset(void *b, int val, size_t len)
{
	size_t	i;
	char	*str;

	str = b;
	i = 0;
	while (i < len)
	{
		str[i] = val;
		i++;
	}
	return (b);
}
